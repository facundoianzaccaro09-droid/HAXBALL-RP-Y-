function inRadius(player, radius) {
  var base = getDisc(player.id);
  if (!base) return [];

  var arr = [];
  room.getPlayerList().forEach(p => {
    var d = getDisc(p.id);
    if (!d) return;
    if (dist(base, d) <= radius) arr.push(p);
  });
  return arr;
}

function rand(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

